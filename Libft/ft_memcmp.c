#include "libft.h"

int	ft_memcmp(const void *b1, const void *b2, size_t n)
{
	const unsigned char		*s1;
	const unsigned char		*s2;

	s1 = b1;
	s2 = b2;
	while (n--)
	{
		if (*s1 != *s2)
			return (*s1 - *s2);
		s1++;
		s2++;
	}
	return (0);
}
